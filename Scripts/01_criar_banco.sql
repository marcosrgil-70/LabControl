-- ============================================================
-- TRIGGER: atualiza saldo ao inserir movimentação
-- ============================================================

DELIMITER $$

CREATE TRIGGER IF NOT EXISTS TRG_MOV_AMOSTRAS_AI
AFTER INSERT ON LAB_MOV_AMOSTRAS
FOR EACH ROW
BEGIN
    IF NEW.E_S = 'E' THEN
        UPDATE LAB_HIST_AMOSTRAS_SALDO
           SET SALDO_ATUAL = SALDO_ATUAL + NEW.QTDE,
               DATA_ATUALIZACAO = NOW()
         WHERE ID_LAB_HIST_AMOSTRAS = NEW.ID_LAB_HIST_AMOSTRAS;
    ELSE
        UPDATE LAB_HIST_AMOSTRAS_SALDO
           SET SALDO_ATUAL = SALDO_ATUAL - NEW.QTDE,
               DATA_ATUALIZACAO = NOW()
         WHERE ID_LAB_HIST_AMOSTRAS = NEW.ID_LAB_HIST_AMOSTRAS;
    END IF;
END$$

DELIMITER ;

