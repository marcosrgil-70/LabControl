CREATE TABLE IF NOT EXISTS EMPRESAS (
    ID_EMPRESAS     INT AUTO_INCREMENT PRIMARY KEY,
    COD_EMPRESAS    VARCHAR(10) NOT NULL,
    ID_ENTIDADES    INT NOT NULL
);

CREATE TABLE IF NOT EXISTS USUARIO (
    USUCOD      INT AUTO_INCREMENT PRIMARY KEY,
    USUNOM      VARCHAR(80) NOT NULL,
    USUSEN      VARCHAR(100) NOT NULL,
    USUADM      TINYINT(1) DEFAULT 0,
    INATIVO     TINYINT(1) DEFAULT 0,
    ID_ENTIDADES INT NULL
);

CREATE TABLE IF NOT EXISTS ACOES (
    USUCOD      INT NOT NULL,
    FORM        VARCHAR(50) NOT NULL,
    INCLUIR     TINYINT(1) DEFAULT 0,
    ALTERAR     TINYINT(1) DEFAULT 0,
    CONSULTAR   TINYINT(1) DEFAULT 0,
    EXCLUIR     TINYINT(1) DEFAULT 0,
    IMPRIMIR    TINYINT(1) DEFAULT 0,
    PRIMARY KEY (USUCOD, FORM),
    FOREIGN KEY (USUCOD) REFERENCES USUARIO(USUCOD)
